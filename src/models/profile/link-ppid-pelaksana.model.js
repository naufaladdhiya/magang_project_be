const mongoose = require("mongoose");

const LinkPPIDPelaksanaSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    link: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

LinkPPIDPelaksanaSchema.method("toJSON", function () {
  const { __v, _id, ...object } = this.toObject();
  object.id = _id;
  return object;
});

module.exports = mongoose.model("LinkPPIDPelaksana", LinkPPIDPelaksanaSchema);
